/**
 * An excellent function that adds two numbers together.
 * @param   {number}  a  The first number.
 * @param   {number}  b  The second number.
 * @returns {number}     a + b
 * @example
 * let x = plus(10, 20);
 * expect(x).to.equal(30);
 */
export function plus(a: number, b: number) {
	return a + b;
}
